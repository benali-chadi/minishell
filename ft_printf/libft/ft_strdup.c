#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*tmp;
	int		len;
	int		i;

	i = 0;
	len = ft_strlen((char *)s);
	if (!(tmp = (char *)malloc(len + 1 * sizeof(char))))
		return (0);
	while (s[i])
	{
		tmp[i] = s[i];
		i++;
	}
	tmp[i] = '\0';
	return (tmp);
}
